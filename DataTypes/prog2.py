# Boolean DataType
data1 = True
data2 = False

print(type(data1))                      # <class 'bool'>

x = 10 
y = 15
print(x == y)                   # False


ans = data1 + data2
print(ans)                      # 1 -> True represent 1     &    False represent 0
print(type(ans))                # <class 'int'>


