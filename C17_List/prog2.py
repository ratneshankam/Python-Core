player = ['rohit', 'shubhman', 'virat', 'shreyas', 'KLrahul', 'surya', 'jaddu']

# Indexing

print(player[2], player[-4])

# Slicing

print(player[2::], '\n', player[2:4:2], '\n', player[:5:3])     # [start:stop:step] in that stop is excluded upto stop-1 operation is performed

print(player[::3], '\n', player[4:2:2], '\n', player[4:2:-1], '\n', player[-1:-5:], '\n', player[-2:-5:-2], '\n', player[-1::-1])

